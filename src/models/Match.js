class Match{

  constructor(id, status, round, date, score, homeTeam, awayTeam){
    this.id = id;
    this.status = status;
    this.round = round;
    this.date = date;
    this.score = score;
    this.homeTeam = homeTeam;
    this.awayTeam = awayTeam;
  }


}

module.exports = Match;